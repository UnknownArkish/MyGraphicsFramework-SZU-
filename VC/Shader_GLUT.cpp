void Shader::SetMatrix4(const GLchar * name, mat4 & matrix, GLboolean useShader)
{
	if (useShader)
		this->Use();
	// 注意由于Angel版本创建的矩阵为正常矩阵，而OpenGL接受的是转置后的矩阵
	// 因此这里需要作转置操作
	glUniformMatrix4fv(glGetUniformLocation(this->ID, name), 1, GL_TRUE, matrix);
}

GLuint Shader::GetAttribLocation(const GLchar * name)
{
	return glGetAttribLocation(this->ID, name);
}

void Shader::checkCompileErrors(GLuint object, std::string type)
{
	GLint success;
	GLchar infoLog[1024];
	if (type != "PROGRAM")
	{
		glGetShaderiv(object, GL_COMPILE_STATUS, &success);
		if (!success)
		{
			glGetShaderInfoLog(object, 1024, NULL, infoLog);
			std::cout << "| ERROR::SHADER: Compile-time error: Type: " << type << "\n"
				<< infoLog << "\n -- --------------------------------------------------- -- "
				<< std::endl;
		}
	}
	else
	{
		glGetProgramiv(object, GL_LINK_STATUS, &success);
		if (!success)
		{
			glGetProgramInfoLog(object, 1024, NULL, infoLog);
			std::cout << "| ERROR::Shader: Link-time error: Type: " << type << "\n"
				<< infoLog << "\n -- --------------------------------------------------- -- "
				<< std::endl;
		}
	}
}

const GLchar* Shader::ReadShaderFile(const GLchar * shaderFilePath)
{
	FILE *fp;
	fopen_s(&fp, shaderFilePath, "r");

	if (fp == NULL) { return NULL; }

	fseek(fp, 0L, SEEK_END);
	long size = ftell(fp);

	fseek(fp, 0L, SEEK_SET);
	char* buf = new char[size + 1];

	memset(buf, 0, size + 1);

	fread(buf, 1, size, fp);

	buf[size] = '\0';
	fclose(fp);

	return buf;
}
